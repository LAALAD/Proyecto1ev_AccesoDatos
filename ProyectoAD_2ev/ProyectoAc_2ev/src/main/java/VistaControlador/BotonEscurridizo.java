    private void botonMoverMouseEntered(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_botonMoverMouseEntered
        turnos++;
        Timer timer = new Timer(150, new ActionListener() { // Espera 500ms antes de moverse
            @Override
            public void actionPerformed(ActionEvent e) {
                Random random = new Random();
                int maxX = getContentPane().getWidth() - botonMover.getWidth();
                int maxY = getContentPane().getHeight() - botonMover.getHeight();

                int x = random.nextInt(Math.max(maxX, 1));
                int y = random.nextInt(Math.max(maxY, 1));

                botonMover.setLocation(x, y);
            }
        });
        timer.setRepeats(false); // Para que solo se ejecute una vez
        timer.start();
    }//GEN-LAST:event_botonMoverMouseEntered

